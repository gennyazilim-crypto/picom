import { readFile, writeFile } from "node:fs/promises";

const LOCK_PATH = new URL("../package-lock.json", import.meta.url);
const REPORT_PATH = new URL("../THIRD_PARTY_LICENSES.generated.md", import.meta.url);
const checkOnly = process.argv.includes("--check");
const lock = JSON.parse(await readFile(LOCK_PATH, "utf8"));
const root = lock.packages?.[""] ?? {};
const directRuntime = new Set(Object.keys(root.dependencies ?? {}));
const directDev = new Set(Object.keys(root.devDependencies ?? {}));

function packageName(path, metadata) {
  if (typeof metadata.name === "string" && metadata.name) return metadata.name;
  return path.split("node_modules/").at(-1) ?? path;
}

function licenseExpression(value) {
  if (typeof value === "string" && value.trim()) return value.trim();
  if (Array.isArray(value) && value.length) return value.join(" OR ");
  return "UNKNOWN";
}

function escapeCell(value) {
  return String(value).replaceAll("|", "\\|").replaceAll("\r", " ").replaceAll("\n", " ");
}

function normalizeLineEndings(value) {
  return value.replaceAll("\r\n", "\n").replaceAll("\r", "\n");
}

const packages = Object.entries(lock.packages ?? {}).flatMap(([path, metadata]) => {
  if (!path || !path.includes("node_modules/")) return [];
  const name = packageName(path, metadata);
  return [{ name, version: metadata.version ?? "UNKNOWN", license: licenseExpression(metadata.license), dependencyClass: directRuntime.has(name) ? "runtime direct" : directDev.has(name) ? "development direct" : metadata.dev === true ? "development transitive" : "runtime transitive" }];
}).sort((left, right) => left.name.localeCompare(right.name) || left.version.localeCompare(right.version));

const missing = packages.filter((item) => item.license === "UNKNOWN");
const blocked = packages.filter((item) => /\b(?:UNLICENSED|PROPRIETARY|COMMERCIAL)\b|SEE LICENSE/i.test(item.license));
const licenseCounts = new Map();
for (const item of packages) licenseCounts.set(item.license, (licenseCounts.get(item.license) ?? 0) + 1);

const report = `# Generated third-party dependency license inventory

> Generated deterministically from \`package-lock.json\` by \`npm run licenses:generate\`. Do not edit this file manually. This inventory is not legal advice and does not replace license-text/obligation review.

## Summary

- Project: ${escapeCell(lock.name ?? "picom")} ${escapeCell(lock.version ?? "unknown")}
- Installed package entries: ${packages.length}
- Unique license expressions: ${licenseCounts.size}
- Missing license metadata: ${missing.length}
- Explicit blocked/proprietary expressions: ${blocked.length}

## License expression counts

| License expression | Packages |
| --- | ---: |
${[...licenseCounts.entries()].sort(([left], [right]) => left.localeCompare(right)).map(([license, count]) => `| ${escapeCell(license)} | ${count} |`).join("\n")}

## Dependency inventory

| Package | Version | Dependency class | License expression |
| --- | --- | --- | --- |
${packages.map((item) => `| ${escapeCell(item.name)} | ${escapeCell(item.version)} | ${item.dependencyClass} | ${escapeCell(item.license)} |`).join("\n")}

## Manual assets and attributions

Non-npm assets such as Coolicons are maintained in \`THIRD_PARTY_NOTICES.md\`. Generated dependency inventory must never remove or replace those attributions.
`;

if (checkOnly) {
  let current = "";
  try { current = await readFile(REPORT_PATH, "utf8"); } catch { /* Report absence is a check failure below. */ }
  const failures = [];
  if (normalizeLineEndings(current) !== normalizeLineEndings(report)) failures.push("Generated license report is missing or stale. Run npm run licenses:generate.");
  if (missing.length) failures.push(`${missing.length} package entries have missing license metadata.`);
  if (blocked.length) failures.push(`${blocked.length} package entries use explicitly blocked/proprietary license metadata.`);
  if (failures.length) { for (const failure of failures) console.error(`FAIL: ${failure}`); process.exit(1); }
  console.log(`PASS: ${packages.length} dependency license entries match the committed report.`);
} else {
  await writeFile(REPORT_PATH, report, "utf8");
  console.log(`Wrote THIRD_PARTY_LICENSES.generated.md with ${packages.length} entries.`);
  if (missing.length || blocked.length) console.warn(`Review required: ${missing.length} missing, ${blocked.length} blocked/proprietary expressions.`);
}
