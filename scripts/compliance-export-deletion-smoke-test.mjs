import fs from "node:fs";

function read(path) {
  return fs.readFileSync(path, "utf8");
}

function assertIncludes(text, expected, label) {
  if (!text.includes(expected)) {
    throw new Error(`${label} missing expected content: ${expected}`);
  }
}

function assertNotMatches(text, pattern, label) {
  if (pattern.test(text)) {
    throw new Error(`${label} contains forbidden pattern: ${pattern}`);
  }
}

const dataExport = read("src/services/dataExportService.ts");
const accountDeletion = read("src/services/accountDeletionService.ts");
const logging = read("src/services/loggingService.ts");
const doc = read("docs/compliance-export-deletion-hardening.md");

assertIncludes(dataExport, "excludedSensitiveFields", "data export service");
assertIncludes(dataExport, "containsCredentials: false", "data export service");
assertIncludes(dataExport, "containsServerSecrets: false", "data export service");
assertIncludes(dataExport, "requiresBackendVerification: true", "data export service");
assertIncludes(dataExport, "sanitizeExportText", "data export service");
assertIncludes(dataExport, "maxExportTextLength", "data export service");

assertIncludes(accountDeletion, "destructiveActionPerformed: false", "account deletion service");
assertIncludes(accountDeletion, "requiresBackendConfirmation: true", "account deletion service");
assertIncludes(accountDeletion, "sessionsRevoked: false", "account deletion service");
assertIncludes(accountDeletion, "ownedCommunitiesRequireTransfer: true", "account deletion service");

assertIncludes(logging, "SENSITIVE_KEY_PATTERN", "logging service");
assertIncludes(logging, "BEARER_PATTERN", "logging service");
assertIncludes(logging, "KEY_VALUE_SECRET_PATTERN", "logging service");

assertIncludes(doc, "No hard deletion is performed by the renderer.", "compliance doc");
assertIncludes(doc, "Production exports should be generated by an authenticated backend job", "compliance doc");
assertIncludes(doc, "Remaining risks", "compliance doc");

for (const [label, text] of [
  ["data export service", dataExport],
  ["account deletion service", accountDeletion],
]) {
  assertNotMatches(text, /console\.(log|warn|error|info)/, label);
  assertNotMatches(text, /passwordHash/i, label);
  assertNotMatches(text, /access_token/i, label);
  assertNotMatches(text, /refresh_token/i, label);
  assertNotMatches(text, /authorizationHeader/i, label);
  assertNotMatches(text, /Authorization:/, label);
  assertNotMatches(text, /service_role/i, label);
}

console.log("Compliance export/deletion smoke test passed.");
