import fs from "node:fs";

function read(path) {
  return fs.readFileSync(path, "utf8");
}

function assertIncludes(text, expected, label) {
  if (!text.includes(expected)) {
    throw new Error(`${label} missing expected content: ${expected}`);
  }
}

function assertNotMatches(text, pattern, label) {
  if (pattern.test(text)) {
    throw new Error(`${label} contains forbidden pattern: ${pattern}`);
  }
}

const dataExport = read("src/services/dataExportService.ts");
const accountDeletion = read("src/services/accountDeletionService.ts");
const logging = read("src/services/logging/loggingService.ts");
const logRedaction = read("src/services/logging/logRedaction.ts");
const doc = read("docs/compliance-export-deletion-hardening.md");

assertIncludes(dataExport, "const excluded", "data export service");
assertIncludes(dataExport, '"password hashes"', "data export service");
assertIncludes(dataExport, '"session tokens"', "data export service");
assertIncludes(dataExport, '"authorization headers"', "data export service");
assertIncludes(dataExport, '"LiveKit credentials"', "data export service");
assertIncludes(dataExport, "normalizeServerPayload", "data export service");
assertIncludes(dataExport, "MAX_SECTION_ROWS = 5000", "data export service");
assertIncludes(dataExport, "excluded: [...excluded]", "data export service");
assertIncludes(dataExport, "data_export_requests", "data export service");
assertIncludes(dataExport, 'client.functions.invoke("user-data-export"', "data export service");

assertIncludes(accountDeletion, "destructiveActionPerformed: false", "account deletion service");
assertIncludes(accountDeletion, "requiresBackendConfirmation: true", "account deletion service");
assertIncludes(accountDeletion, "sessionsRevoked: false", "account deletion service");
assertIncludes(accountDeletion, "ownedCommunitiesRequireTransfer: true", "account deletion service");
assertIncludes(accountDeletion, "expectedUsername", "account deletion service");
assertIncludes(accountDeletion, "ownedCommunityCount", "account deletion service");
assertIncludes(accountDeletion, "account_deletion_requests", "account deletion service");
assertIncludes(accountDeletion, "requested_at", "account deletion service");
assertIncludes(accountDeletion, 'client.functions.invoke<EdgeDeletionResponse>("account-deletion"', "account deletion service");

assertIncludes(logging, "redactLogString", "logging service");
assertIncludes(logging, "redactLogValue", "logging service");
assertIncludes(logRedaction, "SENSITIVE_KEY_PATTERN", "log redaction");
assertIncludes(logRedaction, "BEARER_PATTERN", "log redaction");
assertIncludes(logRedaction, "INLINE_SECRET_PATTERN", "log redaction");

assertIncludes(doc, "No hard deletion is performed by the renderer.", "compliance doc");
assertIncludes(doc, "Production exports should be generated by an authenticated backend job", "compliance doc");
assertIncludes(doc, "Remaining risks", "compliance doc");

for (const [label, text] of [
  ["data export service", dataExport],
  ["account deletion service", accountDeletion],
]) {
  assertNotMatches(text, /console\.(log|warn|error|info)/, label);
  assertNotMatches(text, /passwordHash/i, label);
  assertNotMatches(text, /access_token/i, label);
  assertNotMatches(text, /refresh_token/i, label);
  assertNotMatches(text, /authorizationHeader/i, label);
  assertNotMatches(text, /Authorization:/, label);
  assertNotMatches(text, /service_role/i, label);
}

console.log("Compliance export/deletion smoke test passed.");
